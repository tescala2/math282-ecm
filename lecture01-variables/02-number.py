"""
Three basic types: int, floats, and string
"""

# string
print("hello")
print('hi')

# int
print(5)
print(-2)

# floats
print(3.14)
print(5.0)
print(5e3)

# algebra with int
print(5+3)
5+3 # nothing appears!
print(5-3)
print(5*3)
print(5**2)
print(2**3)

# algebra with floats
print(5.0 + 3.0)
print(5.0 + 3)
print(5 + 3)
print(9.0**0.5)

# algebra with strings!
print('hello'*3)
print('hello'+'see you')
print('hello'+' '+'see you')
print('hello'+'\t'+'see you')
print('hello'+'\n'+'see you')

# division always return a float
print(9.0/3.0)
print(9/3)
print(10/3)

# integer division
print(9//3)
print(10//3)
print(11//3)
print(12//3)

# Limited precision
print(4.99999999999999999999999999999999)
print(1e500)
print(-1e500)

# Conversions
print(float(2))
print(int(2.0))
print(int(2.8))
print(int(-2.8))
print(str(2.8)+str(55))

